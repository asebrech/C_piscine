char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z') &&
				((str[i - 1] >= 58 && str[i - 1] <= 64) ||
				(str[i - 1] >= 91 && str[i - 1] <= 96) ||
				(str[i - 1] < 48 || str[i - 1] > 122)))
			str[i] -= 32;
		i++;
	}
	return (str);
}
