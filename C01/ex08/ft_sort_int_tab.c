void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int swap;

	i = -1;
	while (i++ < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			swap = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = swap;
			i = -1;
		}
	}
}
